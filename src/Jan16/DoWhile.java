public class DoWhile {
    public static void main(String[] args) {
        int i = 5;
        do {
            System.out.println(i);
            i++;
        } while (i<=10);

    int j = 1;
    int k = 5;
        do {
        System.out.println("5 * "+j+" = "+(j*k));
        j++;
    } while (j <= 10);
}}
